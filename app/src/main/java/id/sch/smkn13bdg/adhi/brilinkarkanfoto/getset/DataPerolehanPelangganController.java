public class DataPerolehanPelangganController {

    public String nama_pelanggan;
    public String foto_pelanggan;
    public String jumlah_point;
    public String tanggal_pasif;

    public String getNama_pelanggan() {
        return nama_pelanggan;
    }

    public void setNama_pelanggan(String nama_pelanggan) {
        this.nama_pelanggan = nama_pelanggan;
    }

    public String getFoto_pelanggan() {
        return foto_pelanggan;
    }

    public void setFoto_pelanggan(String foto_pelanggan) {
        this.foto_pelanggan = foto_pelanggan;
    }

    public String getJumlah_point() {
        return jumlah_point;
    }

    public void setJumlah_point(String jumlah_point) {
        this.jumlah_point = jumlah_point;
    }

    public String getTanggal_pasif() {
        return tanggal_pasif;
    }

    public void setTanggal_pasif(String tanggal_pasif) {
        this.tanggal_pasif = tanggal_pasif;
    }
}
